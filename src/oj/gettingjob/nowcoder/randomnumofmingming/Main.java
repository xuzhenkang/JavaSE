package oj.gettingjob.nowcoder.randomnumofmingming;

import java.util.Scanner;
import java.util.TreeMap;

/**
<h2 class="subject-item-title">题目描述</h2>
<div class="subject-describe">
<p>
  明明想在学校中请一些同学一起做一项问卷调查，为了实验的客观性，他先用计算机生成了N个1到1000之间的随机整数（N≤1000），对于其中重复的数字，只保留一个，把其余相同的数去掉，不同的数对应着不同的学生的学号。然后再把这些数从小到大排序，按照排好的顺序去找同学做调查。请你协助明明完成“去重”与“排序”的工作。 </p>
<p>    </p>
<p>    </p>
<p>  Input Param  </p>
<p>       n               输入随机数的个数      </p>
<p>   inputArray      n个随机整数组成的数组  </p>
<p>        </p>
<p>  Return Value </p>
<p>       OutputArray    输出处理后的随机整数 </p>
<p>    </p>
<p>
  <br> 注：测试用例保证输入参数的正确性，答题者无需验证。测试用例不止一组。 </p>
<p>
  <br>   </p>
<p>    </p>
<br/>
<h5><b>输入描述:</b></h5>
<pre><p>输入多行，先输入随机整数的个数，再输入相应个数的整数</p></pre>
<br/>
<br/>
<h5><b>输出描述:</b></h5>
<pre><p>返回多行，处理后的结果</p></pre>
<br/>
<h5><b>输入例子:</b></h5>
<pre>11
10
20
40
32
67
40
20
89
300
400
15
</pre>
<br/>
<h5><b>输出例子:</b></h5>
<pre>10
15
20
32
40
67
89
300
400
</pre>
</div>
 * @author lenovo
 *
 */
public class Main {
	public static void main(String[] args) {
		Scanner cin = new Scanner(System.in);
		TreeMap<Integer, Integer> tm = new TreeMap<Integer, Integer>();
		int n = 0;
		while (cin.hasNext()) {
			tm.clear();
			n = cin.nextInt();
			for (int i = 0; i < n; i++) {
				int num = cin.nextInt();
				tm.put(num, num);
			}
			while (tm.size() > 0) {
				System.out.println(tm.get(tm.firstKey()));
				tm.remove(tm.firstKey());
			}
		}
		cin.close();
	}
}
